export function getShape() {
  const graphVal = graph.value as Graph
  const commonAttrs = {
    body: {
      fill: '#fff',
      stroke: '#613400',
      strokeWidth: 1,
    },
  }
  const rectangle = graphVal.createNode({
    shape: 'rect', // 矩形。
    width: 80,
    height: 40,
    label: '矩形',
    attrs: commonAttrs,
  })
  return [rectangle]
}
